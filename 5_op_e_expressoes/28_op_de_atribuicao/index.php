<?php 

    // +=
    $a = 0;
    $a += 10; // $a = $a + 10

    echo $a . "<br>";

    // -=
    $a = 0;
    $a -= 10; // $a = $a - 10

    echo $a . "<br>";    
    
    // *=
    $a = 2;
    $a *= 10; // $a = $a * 10

    echo $a . "<br>";
    
    // /=
    $a = 5;
    $a /= 10; // $a = $a / 10

    echo $a . "<br>";

    //ultilização dom variáveis

    $f = 10;
    $x = 20;

    $f += $x; // $f = $f + $x

    echo $f;

?>